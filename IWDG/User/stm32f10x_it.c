#include "stm32f10x_it.h"
#include	"iwdg.h"

void	EXTI1_IRQHandler(void)
{
	if(EXTI_GetITStatus(EXTI_Line1)!=0)
	{
		EXTI_ClearITPendingBit(EXTI_Line1);
		IWDG_ReloadCounter();
		GPIOC->BRR	=	1<<1;
	}
}

